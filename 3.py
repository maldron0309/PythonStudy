# 2. 논리 자료형(Boolean Data)   
# 
# **Point I**  
# 논리 자료형 : 참(True) 혹은 거짓(False)을 나타내는 자료형  
# 
# True, False  
# 
# **Point II**  
# 비교 연산자 : 숫자나 문자의 값을 비교하는 연산자  
# 주어진 진술이 참이면 True, 거짓이면 False  
# 
# >A == B #A와 B가 같다  
# A != B #A와 B가 다르다  
# A >= B #A가 B보다 크거나 같다  
# A <= B #A가 B보다 작거나 같다  
# A > B #A가 B보다 크다  
# A < B #A가 B보다 작다  
# >
# >print(3 == 3) #True  
# print(3 != 3) #False  
# 
# **Point III**  
# 논리 연산자 : 논리 자료형 사이의 연산  
# 
# * AND : 각 논리가 모두 True이면 결과가 True    
# * OR : 각 논리 중 True가 존재하면 결과가 True    
# * NOT : 논리값을 뒤집는 연산  
# 
