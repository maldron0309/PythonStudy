# 학생의 시험 점수를 입력받습니다.
score = int(input("시험 점수를 입력하세요: "))

# 시험 점수에 따라 학점을 부여합니다.
if score >= 88:
    print("A+")
elif score >= 77:
    print("A0")
elif score == 0:
    print("F")
else:
    print("B+")

