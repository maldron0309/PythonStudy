# 3. 조건문  
# 조건에 따라 특정 명령을 수행하는 구문  
# 
# **Point I**  
# if문 : 조건이 참이면 명령을 수행  
# 
# >if a >= 5:  
# &nbsp;&nbsp;&nbsp;&nbsp;    print("a는 5 이상입니다!")  
# 
# **Point II**
# elif문 : 이전 조건이 거짓인 상황에서 조건이 참이면 명령을 수행  
# 
# >if a >= 5:  
# &nbsp;&nbsp;&nbsp;&nbsp;    print("a는 5 이상입니다!")  
# elif a >= 3:  
# &nbsp;&nbsp;&nbsp;&nbsp;    print("a는 3 이상 5 미만입니다!")  
# 
# **Point III**  
# else문 : 위의 조건에 해당하지 않는 모든 경우에 수행  
# 
# >if a >= 5:  
